void delay_us(uint16_t nus)
{
	uint16_t us=65535-nus-5;  //防止意外配置 重装载和中断 导致计数不准
	HAL_TIM_Base_Start(&htim14);
	__HAL_TIM_SetCounter(&htim14,us);
	
	while(us<65530)
	{
		us=__HAL_TIM_GetCounter(&htim14);
	}
	HAL_TIM_Base_Stop(&htim14);
	
}
